import pytest

def test_tire_subject_predicate():
    sentences = test1.strip().split('\n')
    for sent in sentences:
        # Проверяем наличие конструкции "подлежащее — сказуемое"
        assert " — " in sent, f"В предложении отсутствует тире: {sent}"
        parts = sent.split(" — ")
        assert len(parts) == 2, f"Некорректное разделение подлежащего и сказуемого: {sent}"

def test_direct_speech_colon():
    sentences = test6.strip().split('\n')
    for sent in sentences:
        # Проверяем наличие двоеточия перед прямой речью
        assert ":" in sent, f"Отсутствует двоеточие в прямой речи: {sent}"
        assert "«" in sent and "»" in sent, f"Нет кавычек прямой речи: {sent}"

def test_homogeneous_members():
    sentences = test10.strip().split('\n')
    for sent in sentences:
        # Проверяем наличие минимум 3 однородных членов
        parts = sent.split(", ")
        assert len(parts) >= 3, f"Недостаточно однородных членов: {sent}"

def test_complex_sentences():
    sentences = test8.strip().split('\n')
    for sent in sentences:
        # Проверяем наличие союзов и сложной структуры
        assert any(conj in sent for conj in ["и", "но", "что", "потому что"]), f"Нет союзных конструкций: {sent}"
        assert sent.count(",") >= 2, f"Слишком простая структура предложения: {sent}"

def test_isolated_constructions():
    sentences = test14.strip().split('\n')
    for sent in sentences:
        # Проверяем обособленные определения
        assert sent.count(",") >= 2, f"Нет обособленных конструкций: {sent}"
        assert any(word in sent for word in ["окружённый", "подаренная", "сидящий"]), "Нет причастных оборотов"

# Пример параметризованного теста для нескольких случаев
@pytest.mark.parametrize("test_data,expected_sign", [
    (test3, " — "),
    (test5, " — "),
    (test6, ": «"),
    (test18, ": «"),
])
def test_punctuation_marks(test_data, expected_sign):
    sentences = test_data.strip().split('\n')
    for sent in sentences:
        assert expected_sign in sent, f"Отсутствует требуемый знак препинания: {sent}"

# Тест для риторических вопросов
def test_rhetorical_questions():
    sentences = test16.strip().split('\n')
    for sent in sentences:
        assert sent.endswith("?"), f"Отсутствует вопросительный знак: {sent}"
        assert any(word in sent for word in ["Кто", "Разве", "Как"]), "Нет риторического вопроса"

# Тест для сравнительных оборотов
def test_comparisons():
    sentences = test15.strip().split('\n')
    for sent in sentences:
        assert any(conj in sent for conj in ["как", "словно", "подобно"]), f"Нет сравнения: {sent}"
        assert " — " in sent or "," in sent, "Нет разделения оборота"

# Тест для разных стилей речи
def test_speech_styles():
    sentences = test27.strip().split('\n')
    styles = ["(Художественный)", "(Научный)", "(Публицистический)", "(Разговорный)"]
    for sent, style in zip(sentences, styles):
        assert style in sent, f"Неверный стиль: {sent}"
        assert len(sent) > len(style) + 2, "Пустое содержание для стиля"

# Тест для парцелляции
def test_parcellation():
    sentences = test17.strip().split('\n')
    assert len(sentences) >= 5, "Недостаточно примеров парцелляции"
    for sent in sentences:
        assert len(sent.split()) < 8, "Слишком длинное предложение для парцелляции"


